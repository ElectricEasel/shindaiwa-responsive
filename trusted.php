						<p>The history of M. Kawamura Farm Enterprises is so deep and colorful that it is almost easy to forget the dealership is located in the middle of paradise.</p>
						<p>The dealership's story starts with Mac Kawamura, a Japanese-American who started his career as the herbicide and rodent supervisor at Hawaii's Lihu'e sugar plantation. Mac was so good at controlling rodents that he earned the nickname of "The Rodent Man" in the sugar industry.</p>
						<p>Mac's entrepreneurial and innovative spirit led to his development of a special type of rat bait that he used in the sugar fields. He went on to patent that bait, and eventually started selling enough to the local plantations that he opened his own store, M. Kawamura Farm Enterprises, in 1961.</p>
						<img class="article-img-3 hide-desktop" src="images/temp/kawamura-3.png" alt="" />
						<p>A few years earlier, in 1958, Mac's son, Ed, graduated from high school and went off to join the U.S. Army. While stationed in Germany, Ed met his wife, Lily, with whom he had four children.</p>
						<p>During his enlistment, Ed served one tour of duty in Vietnam, where he was responsible for seven radio communications sites. He went on to serve in the Army for 20 years, until his retirement in 1978.</p>
						<img class="article-img-2" src="images/temp/kawamura-2.jpg" alt="" />
						<p>As the son of a family-owned business owner, Ed felt compelled to come back to Kawamura Farm to help his father. After taking over for Mac, Ed realized there was more opportunity to serve people on the island, and began selling animal supplies, seed, and power equipment – including Shindaiwa Outdoor Power Equipment.</p>
						<p>Ed also felt another strong conviction – to the veterans who have served this country. Ed became very active in veterans affairs and lobbying for veterans' benefits. His dedication to veterans on the island helped build the Kauai Veterans Center. Ed is currently the commander of the Kauai Veterans Council, and he spends a lot of his days driving the island's Disabled American Veterans van.</p>
						<p>When it comes to success in business, Ed says the key to his success has been his dedication to his customers. "They know that I will always stand up for what's right," he says. "Shindaiwa has always been a stand-up product for me. They were there for me when I was just getting started and needed their support, and there was no doubt in my mind that Shindaiwa makes a great product.</p>
						<img class="article-img-1" src="images/temp/kawamura-1.png" alt="" />
						<p>&ldquo;My dad always said to have pride in your name because that's the only thing you have," he continues. "With my customers, I want to sell them equipment where the name stands on its own. The name of the equipment we sell is as important as the name on our door. For me, the name Shindaiwa has always meant quality, durability and maintainability – and that means a lot.&rdquo;</p>
						<p>Today, Kawamura Farm is just as much a family affair as it ever was. There are now three generations of Kawamuras working at the dealership. Though Daniel, one of Ed's sons, jokes that he spends more time with the veterans, these days, than he does at the farm.</p>
