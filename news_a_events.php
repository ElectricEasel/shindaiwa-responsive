									<h1 class="contact__caption">Green Industry Pros.com Reviews the Shindaiwa T235 Trimmer</h1>
									<time class="datetime" datetime="2014-5-20">5/20/2014</time>

									<p class="italic-style">Green Industry Pro testers Nathan Mitchell of All-Pro Mowing & Landscaping, and Brandon Pedersen of Cutting Edge Landscaping (both of Amarillo, Texas), have lots of praise for the new T235 trimmer.  Read what they have to say
										<a href="">here</a>....</p>
									<p>Shindaiwa says its <a href="">T235 String Trimmer</a> balances “legendary” Shindaiwa features with improved cutting performance at a great price. Two landscape contractors in Amarillo, TX, tend to agree with that statement, while adding that the T235's ability to handle ethanol-blended fuel has met a growingly important need.</p>
									<p>Nathan Mitchell of All-Pro Mowing & Landscaping was having carburetor issues with another brand/model of trimmer. "I was having to clean the carburetor at least twice a month," Mitchell relates. "The trimmer was also losing power. I talked to my equipment dealer who said it was probably the ethanol-blended fuel I was putting in it. Also, you have to be very precise on the fuel/oil mix with most two-cycle string trimmers. But on the Shindaiwa T235, the dealer said you don't have to be quite so precise."</p>
									<p>John Powers, senior product manager for ECHO Incorporated (maker of both ECHO- and Shindaiwa-branded equipment), says the Shindaiwa T235, among other Shindaiwa trimmers, is engineered to run on 10% ethanol. That's why Mitchell hasn't seen the same type of carburetor issues with his T235 since purchasing it this past August. Performance-wise, Mitchell says the T235 does a fine job. It is also a bit lighter than other trimmers he's used, which is a plus.</p>
									<p>Brandon Pedersen of Cutting Edge Landscaping bought a handful of T235s this past spring. "We have so much small equipment I can't keep track of it all," he says with a chuckle. But Pedersen does know full well that his T235s have delivered. "The trigger is really nice and easy to operate," he points out. "It's also pretty lightweight compared to some trimmers."</p>
									<p>Like Mitchell, Pedersen was also having carburetor/ethanol issues with past trimmer models. "What I like about the T235 is that there is more space between the carburetor and the rest of the engine," Pedersen relates. "There's probably an inch gap. So the carburetor doesn't get as hot, which is helpful when burning ethanol. Even on other Shindaiwa trimmer models I've used, that gap seems to be much more narrow."</p>
									<p>For contractors like Pedersen and Mitchell, engine performance and user comfort are among the primary concerns with respect to handheld equipment like string trimmers. Powers points out that the T235 features a secondary air filter for improved air filtration. Additionally, a solid steel drive shaft helps reduce weight, enhance acceleration and reduce vibration.</p>
									<p>After several months of commercial use, both Pedersen and Mitchell say they would buy additional Shindaiwa T235 trimmers in the future.</p>

